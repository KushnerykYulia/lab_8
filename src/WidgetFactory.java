/**
 * Інтерфейс WidgetFactory визначає методи для створення компонентів графічного інтерфейсу:
 * кнопок та текстових полів. Клієнт працює лише через цей інтерфейс.
 */
interface WidgetFactory {
    /**
     * Метод для створення кнопки.
     * @return конкретний об'єкт кнопки
     */
    Button createButton();

    /**
     * Метод для створення текстового поля.
     * @return конкретний об'єкт текстового поля
     */
    TextField createTextField();
}
